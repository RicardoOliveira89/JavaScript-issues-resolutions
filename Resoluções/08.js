// 8. Escreva um programa que leia o valor de uma temperatura em Celsius 
// e calcule o seu valor correspondente em Fahrenheit e em Kelvin.

let celsius = 25;

let fahrenheit = (celsius * 1.8) + 32;
let kelvin = celsius + 273.15;

console.log("\nFahrenheit = " + fahrenheit + "\nKelvin = " + kelvin + "\n");